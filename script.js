            navMenu.style.right = '';
            navMenu.style.background = '';
            navMenu.style.flexDirection = '';
            navMenu.style.padding = '';
            navMenu.style.boxShadow = '';
            navMenu.style.borderRadius = '';
            
            mobileToggle.classList.remove('active');
